SELECT DISTINCT
CONSERVATION_PROGRAM.CONSERVATION_PROGRAM_CODE CNSV_PGM_CD,
CONSERVATION_PROGRAM.CONSERVATION_PROGRAM_DESCRIPTION CNSV_PGM_DESC,
CONSERVATION_PROGRAM.DATA_STATUS_CODE DATA_STAT_CD,
CONSERVATION_PROGRAM.CREATION_DATE CRE_DT,
CONSERVATION_PROGRAM.CREATION_USER_NAME CRE_USER_NM,
CONSERVATION_PROGRAM.LAST_CHANGE_DATE LAST_CHG_DT,
CONSERVATION_PROGRAM.LAST_CHANGE_USER_NAME LAST_CHG_USER_NM,
OP AS CDC_OPER_CD,
1 AS row_num_part
FROM CONSERVATION_PROGRAM
WHERE OP <> 'D'
UNION
SELECT DISTINCT
CONSERVATION_PROGRAM.CONSERVATION_PROGRAM_CODE CNSV_PGM_CD,
CONSERVATION_PROGRAM.CONSERVATION_PROGRAM_DESCRIPTION CNSV_PGM_DESC,
CONSERVATION_PROGRAM.DATA_STATUS_CODE DATA_STAT_CD,
CONSERVATION_PROGRAM.CREATION_DATE CRE_DT,
CONSERVATION_PROGRAM.CREATION_USER_NAME CRE_USER_NM,
CONSERVATION_PROGRAM.LAST_CHANGE_DATE LAST_CHG_DT,
CONSERVATION_PROGRAM.LAST_CHANGE_USER_NAME LAST_CHG_USER_NM,
'D' CDC_OPER_CD,
1 AS row_num_part
FROM CONSERVATION_PROGRAM
WHERE OP = 'D'