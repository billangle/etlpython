SELECT DISTINCT
CONTRACT_ACTIVITY_TYPE.CONTRACT_ACTIVITY_TYPE_CODE CTR_ACTV_TYPE_CD,
CONTRACT_ACTIVITY_TYPE.CONTRACT_ACTIVITY_TYPE_DESCRIPTION CTR_ACTV_TYPE_DESC,
CONTRACT_ACTIVITY_TYPE.DATA_STATUS_CODE DATA_STAT_CD,
CONTRACT_ACTIVITY_TYPE.CREATION_DATE CRE_DT,
CONTRACT_ACTIVITY_TYPE.CREATION_USER_NAME CRE_USER_NM,
CONTRACT_ACTIVITY_TYPE.LAST_CHANGE_DATE LAST_CHG_DT,
CONTRACT_ACTIVITY_TYPE.LAST_CHANGE_USER_NAME LAST_CHG_USER_NM,
OP AS CDC_OPER_CD,
1 AS row_num_part
FROM CONTRACT_ACTIVITY_TYPE
WHERE OP <> 'D'
UNION
