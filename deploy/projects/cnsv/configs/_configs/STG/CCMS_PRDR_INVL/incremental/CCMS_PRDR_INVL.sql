SELECT DISTINCT
PRODUCER_INVOLVEMENT.PRODUCER_INVOLVEMENT_CODE PRDR_INVL_CD,
PRODUCER_INVOLVEMENT.PRODUCER_INVOLVEMENT_DESCRIPTION PRDR_INVL_DESC,
PRODUCER_INVOLVEMENT.DATA_STATUS_CODE DATA_STAT_CD,
PRODUCER_INVOLVEMENT.CREATION_DATE CRE_DT,
PRODUCER_INVOLVEMENT.CREATION_USER_NAME CRE_USER_NM,
PRODUCER_INVOLVEMENT.LAST_CHANGE_DATE LAST_CHG_DT,
PRODUCER_INVOLVEMENT.LAST_CHANGE_USER_NAME LAST_CHG_USER_NM,
OP AS CDC_OPER_CD,
1 AS row_num_part
FROM PRODUCER_INVOLVEMENT
WHERE OP <> 'D'
UNION
SELECT DISTINCT
PRODUCER_INVOLVEMENT.PRODUCER_INVOLVEMENT_CODE PRDR_INVL_CD,
PRODUCER_INVOLVEMENT.PRODUCER_INVOLVEMENT_DESCRIPTION PRDR_INVL_DESC,
PRODUCER_INVOLVEMENT.DATA_STATUS_CODE DATA_STAT_CD,
PRODUCER_INVOLVEMENT.CREATION_DATE CRE_DT,
PRODUCER_INVOLVEMENT.CREATION_USER_NAME CRE_USER_NM,
PRODUCER_INVOLVEMENT.LAST_CHANGE_DATE LAST_CHG_DT,
PRODUCER_INVOLVEMENT.LAST_CHANGE_USER_NAME LAST_CHG_USER_NM,
'D' CDC_OPER_CD,
1 AS row_num_part
FROM PRODUCER_INVOLVEMENT
WHERE OP = 'D'