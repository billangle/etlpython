SELECT DISTINCT
TERMINATION_PROCESSING.TERMINATION_PROCESSING_CODE TERM_PROC_CD,
TERMINATION_PROCESSING.TERMINATION_PROCESSING_DESCRIPTION TERM_PROC_DESC,
TERMINATION_PROCESSING.DATA_STATUS_CODE DATA_STAT_CD,
TERMINATION_PROCESSING.CREATION_DATE CRE_DT,
TERMINATION_PROCESSING.CREATION_USER_NAME CRE_USER_NM,
TERMINATION_PROCESSING.LAST_CHANGE_DATE LAST_CHG_DT,
TERMINATION_PROCESSING.LAST_CHANGE_USER_NAME LAST_CHG_USER_NM,
OP AS CDC_OPER_CD,
1 AS row_num_part
FROM TERMINATION_PROCESSING
WHERE OP <> 'D'
UNION
SELECT DISTINCT
TERMINATION_PROCESSING.TERMINATION_PROCESSING_CODE TERM_PROC_CD,
TERMINATION_PROCESSING.TERMINATION_PROCESSING_DESCRIPTION TERM_PROC_DESC,
TERMINATION_PROCESSING.DATA_STATUS_CODE DATA_STAT_CD,
TERMINATION_PROCESSING.CREATION_DATE CRE_DT,
TERMINATION_PROCESSING.CREATION_USER_NAME CRE_USER_NM,
TERMINATION_PROCESSING.LAST_CHANGE_DATE LAST_CHG_DT,
TERMINATION_PROCESSING.LAST_CHANGE_USER_NAME LAST_CHG_USER_NM,
'D' CDC_OPER_CD,
1 AS row_num_part
FROM TERMINATION_PROCESSING
WHERE OP = 'D'