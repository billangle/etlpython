ACCOUNTING_PROGRAM.ACCOUNTING_PROGRAM_DESCRIPTION ACCT_PGM_DESC,
ACCOUNTING_PROGRAM.ACCOUNTING_PROGRAM_IDENTIFIER ACCT_PGM_ID,
ACCOUNTING_PROGRAM.PAYMENT_PROGRAM_CODE PYMT_PGM_CD,
ACCOUNTING_PROGRAM.PROCESSING_SEQUENCE_NUMBER PROC_SEQ_NBR,
