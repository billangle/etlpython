EDV.FARM_TR_YR_LS(FARM_TR_L_ID,TR_YR_ID,LOAD_DT,
DATA_EFF_STRT_DT,
DATA_SRC_NM,
DATA_STAT_CD,
SRC_CRE_DT,
SRC_LAST_CHG_DT,
SRC_LAST_CHG_USER_NM,
TR_ID,
FARM_YR_ID,
FMLD_ACRG,
CPLD_ACRG,
CRP_ACRG,
MPL_ACRG,
WBP_ACRG,
