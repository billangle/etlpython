SELECT LTrim(RTrim(tract_producer_year.county_fsa_code) ) As CNTY_FSA_CD,
tract_producer_year.core_customer_identifier As CORE_CUST_ID,
tract_producer_year.creation_date As CRE_DT,
LTrim(RTrim(tract_producer_year.data_status_code) ) As DATA_STAT_CD,
tract_producer_year.farm_identifier As FARM_ID,
LTrim(RTrim(tract_producer_year.farm_number) ) As FARM_NBR,
tract_producer_year.last_change_date As LAST_CHG_DT,
LTrim(RTrim(tract_producer_year.last_change_user_name) ) As LAST_CHG_USER_NM,
tract_producer_year.producer_involvement_code As PRDR_INVL_CD,
tract_producer_year.producer_involvement_end_date As PRDR_INVL_END_DT,
LTrim(RTrim(tract_producer_year.producer_involvement_interrupted_indicator) ) As PRDR_INVL_INTRPT_IND,
tract_producer_year.producer_involvement_start_date As PRDR_INVL_STRT_DT,
LTrim(RTrim(tract_producer_year.state_fsa_code) ) As ST_FSA_CD,
tract_producer_year.time_period_identifier As TM_PRD_ID,
LTrim(RTrim(tract_producer_year.tract_number) ) As TR_NBR,
tract_producer_year.tract_producer_cw_exception_code As TR_PRDR_CW_EXCP_CD,
tract_producer_year.tract_producer_hel_exception_code As TR_PRDR_HEL_EXCP_CD,
tract_producer_year.tract_producer_pcw_exception_code As TR_PRDR_PCW_EXCP_CD,
tract_producer_year.tract_producer_year_identifier As TR_PRDR_YR_ID,
tract_producer_year.tract_year_identifier As TR_YR_ID,
time_period.time_period_name As PGM_YR,
tract_producer_year.hel_appeals_exhausted_date As HEL_APLS_EXHST_DT, 
tract_producer_year.cw_appeals_exhausted_date As CW_APLS_EXHST_DT, 
tract_producer_year.pcw_appeals_exhausted_date As PCW_APLS_EXHST_DT, 
tract_producer_year.tract_producer_rma_hel_exception_code As TR_PRDR_RMA_HEL_EXCP_CD, 
tract_producer_year.tract_producer_rma_cw_exception_code As TR_PRDR_RMA_CW_EXCP_CD, 
tract_producer_year.tract_producer_rma_pcw_exception_code As TR_PRDR_RMA_PCW_EXCP_CD 
FROM farm_records_reporting.tract_producer_year
LEFT JOIN farm_records_reporting.time_period ON ( tract_producer_year.time_period_identifier = time_period.time_period_identifier)