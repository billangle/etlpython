SELECT  PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT_IDENTIFIER As PRDR_PGM_PYMT_LMT_ADJ_ID,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.SUBSIDIARY_CUSTOMER_IDENTIFIER As SBSD_CUST_ID,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.LIMITED_PAYMENT_PROGRAM_PERIOD_IDENTIFIER As PYMT_PGM_PRD_ID,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.PRODUCER_PAYMENT_LIMITATION_ADJUSTMENT_TYPE_CODE As PRDR_PYMT_LMT_ADJ_TYPE_CD,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.PAYMENT_LIMITATION_ADJUSTMENT_AMOUNT As PYMT_LMT_ADJ_AMT,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.NOTE_TEXT As NOTE_TXT,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.DATA_STATUS_CODE As DATA_STAT_CD,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.CREATION_DATE As CRE_DT,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.CREATION_USER_NAME As CRE_USER_NM,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.LAST_CHANGE_DATE As LAST_CHG_DT,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.LAST_CHANGE_USER_NAME As LAST_CHG_USER_NM,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.TRANSMISSION_STATUS_CODE As TRNSM_STAT_CD,
SUBSIDIARY_CUSTOMER.CORE_CUSTOMER_IDENTIFIER As CORE_CUST_ID,
LIMITED_PAYMENT_PROGRAM.PROGRAM_NAME As PGM_NM,
LIMITED_PAYMENT_PROGRAM_PERIOD.SUBSIDIARY_PERIOD_START_YEAR As SBSD_PRD_STRT_YR,
PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.OP
from `fsa-{env}-sbsd-cdc`.`PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT` 
LEFT OUTER JOIN  `fsa-{env}-sbsd-cdc`.`SUBSIDIARY_CUSTOMER`
ON SUBSIDIARY_CUSTOMER.SUBSIDIARY_CUSTOMER_IDENTIFIER = PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.SUBSIDIARY_CUSTOMER_IDENTIFIER 
LEFT OUTER JOIN `fsa-{env}-sbsd-cdc`.`LIMITED_PAYMENT_PROGRAM_PERIOD`
ON LIMITED_PAYMENT_PROGRAM_PERIOD.LIMITED_PAYMENT_PROGRAM_PERIOD_IDENTIFIER = PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.LIMITED_PAYMENT_PROGRAM_PERIOD_IDENTIFIER 
LEFT OUTER JOIN `fsa-{env}-sbsd-cdc`.`LIMITED_PAYMENT_PROGRAM`
ON LIMITED_PAYMENT_PROGRAM.LIMITED_PAYMENT_PROGRAM_IDENTIFIER = LIMITED_PAYMENT_PROGRAM_PERIOD.LIMITED_PAYMENT_PROGRAM_IDENTIFIER
WHERE CAST(PRODUCER_PROGRAM_PAYMENT_LIMITATION_ADJUSTMENT.dart_filedate as DATE) BETWEEN DATE '{ETL_START_DATE}' AND DATE '{ETL_END_DATE}' 